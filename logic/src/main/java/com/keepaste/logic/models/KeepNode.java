package com.keepaste.logic.models;

import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import com.fasterxml.jackson.annotation.JsonSubTypes;
import com.fasterxml.jackson.annotation.JsonTypeInfo;
import lombok.AllArgsConstructor;
import lombok.Getter;
import lombok.NoArgsConstructor;
import lombok.Setter;
import lombok.experimental.SuperBuilder;

import javax.swing.*;

/**
 * This class represents a Keep node, it holds what is common for both {@link Keep} and {@link KeepsGroup}.
 */
@SuperBuilder
@Getter
@Setter
@AllArgsConstructor
@NoArgsConstructor
@JsonIgnoreProperties(ignoreUnknown = true)
@JsonTypeInfo(use = JsonTypeInfo.Id.NAME, include = JsonTypeInfo.As.PROPERTY)
@JsonSubTypes({
        @JsonSubTypes.Type(value = KeepsGroup.class, name = "KeepsGroup"),

        @JsonSubTypes.Type(value = Keep.class, name = "Keep") }
)
public abstract class KeepNode {
    private String title;
    private Icon icon;
}
